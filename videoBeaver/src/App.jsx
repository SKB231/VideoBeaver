  const [s3Stuff, setS3Stuff] = useState("")

  const onFileUpload = async (event) => {
    const file = event.target.files[0]
    if (!file) return

    const response = await fetch('/upload', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ filename: file.name, file_size: file.size }),
    })

    if (!response.ok) {
      const err = await response.json()
      console.error('Upload request failed:', err.detail)
      return
    }

    //const resp = await response.json()
    const { url, fields } = await response.json()

    //setS3Stuff(resp)

    const formData = new FormData()
    Object.entries(fields).forEach(([k, v]) => formData.append(k, v))
    formData.append('file', file)
    console.log(url, fields)

    const s3Response = await fetch(url, { method: 'POST', body: formData })
    console.log('S3 upload status:', s3Response.status)
  }

